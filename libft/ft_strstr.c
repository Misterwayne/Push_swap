char	*ft_strstr(char *str, char *to_find)
{
	int j;

	j = 0;
	if (to_find[j] == '\0')
		return (str);
	while (*str != '\0')
	{
		j = 0;
		while (str[j] == to_find[j])
		{
			if (to_find[j + 1] == '\0')
				return (str);
			j++;
		}
		str++;
	}
	return (0);
}
